# =============================================
# متغيرات عامة
# =============================================
collapse_triggered = False
collapse_year = None

# التمكين (ك) – متغير تابع (جزاء دنيوي)
ك = 0.2
history_ك = [ك]
history_S = []
history_absolute_deviation = []

# *** طبقة الآخرة (جزاء أخروي) – "صحيفة الأعمال" ***
# هذه المتغيرات "مخفية" لا تظهر في الدنيا، ولكنها تتراكم لليوم الآخر
اخرة = {
    'حسنات': 0,  # رصيد الحسنات المتراكمة
    'سيئات': 0,  # رصيد السيئات المتراكمة
    'ميزان_الاخرة': [] # تاريخ الميزان الأخروي (حسنات - سيئات)
}

# =============================================
# دالة الميزان – الصياغة الرياضية الدقيقة
# =============================================
def calculate_mizan(نور, ظلام, ك_prev, اخرة):
    global collapse_triggered, collapse_year
    ق_val = الثوابت['ق']['value']
    ن_val = الثوابت['ن']['value']
    ص_val = الثوابت['ص']['value']
    ك_val = الثوابت['ك']['value']

    # 1. دالة الطاقة النورانية (Φ – Phi) – الجزاء الدنيوي
    phi = ق_val * np.log(1 + ن_val * نور['أ']['value'] + ص_val * نور['هـ']['value'])
    
    # 2. دالة الطاقة الظلامية (Ψ – Psi)
    psi = 0
    for key in ظلام: psi += ظلام[key]['value']
    psi = np.exp(psi / len(ظلام)) - 1

    # 3. دالة "الاستدراج" (الكَسْر المادي E)
    delta_psi_from_E = ك_val * (ك_prev ** 2)
    psi += delta_psi_from_E

    # 4. قوة الميزان (S)
    S = phi / (psi + 0.01)
    
    # 5. تحديث التمكين (ك)
    target_ك = ق_val / 100 * S
    ك_new = ك_prev + 0.05 * (target_ك - ك_prev)
    ك_new = max(0.01, min(2.0, ك_new))
    
    # *** 6. طبقة الآخرة – تحديث صحيفة الأعمال ***
    # لكل "فعل" في الدنيا (قيم النور)، "رد فعل" في الآخرة (حسنات)
    # لكل "فعل" سيء (قيم الظلام)، "رد فعل" في الآخرة (سيئات)
    delta_حسنات = np.mean([نور[k]['value'] for k in نور]) * 0.1
    delta_سيئات = np.mean([ظلام[k]['value'] for k in ظلام]) * 0.1
    
    اخرة['حسنات'] += delta_حسنات
    اخرة['سيئات'] += delta_سيئات
    اخرة['ميزان_الاخرة'].append(اخرة['حسنات'] - اخرة['سيئات'])
    
    return S, ك_new

# =============================================
# آليات التحديث – تفاعلات القوى
# =============================================
def update_world(نور, ظلام, ك_prev):
    # 1. تأثير النور على بعضه
    نور['أ']['value'] += 0.01 * نور['ر']['value'] - 0.02 * ظلام['ش']['value'] - 0.01 * ظلام['ت']['value']
    نور['هـ']['value'] += 0.01 * نور['ر']['value'] - 0.02 * ظلام['ذ']['value'] - 0.01 * ظلام['ت']['value']
    نور['ع']['value'] += 0.01 * نور['ل']['value'] - 0.01 * ظلام['ج']['value']
    نور['ل']['value'] += 0.01 * نور['ص']['value'] - 0.01 * ظلام['ظ']['value']
    نور['ر']['value'] += 0.01 * نور['ص']['value'] - 0.02 * ظلام['خ']['value']
    نور['ح']['value'] += 0.01 * نور['ط']['value'] - 0.01 * ظلام['د']['value']
    نور['ط']['value'] += 0.01 * نور['ع']['value'] - 0.01 * ظلام['ف']['value']
    نور['م']['value'] += 0.005 * نور['أ']['value'] * نور['هـ']['value']
    
    # 2. تأثير الظلام على بعضه
    ظلام['ب']['value'] += 0.01 * ظلام['ج']['value'] + 0.01 * ظلام['ض']['value']
    ظلام['ت']['value'] += 0.01 * ظلام['ذ']['value'] + 0.01 * ظلام['ظ']['value']
    ظلام['ث']['value'] += 0.01 * ظلام['ج']['value'] + 0.01 * ظلام['ض']['value']
    ظلام['ج']['value'] += 0.01 * ظلام['ض']['value'] + 0.01 * ظلام['ب']['value']
    ظلام['خ']['value'] += 0.01 * ظلام['غ']['value'] + 0.01 * ظلام['ف']['value']
    ظلام['د']['value'] += 0.01 * ظلام['ظ']['value'] + 0.01 * ظلام['خ']['value']
    ظلام['ذ']['value'] += 0.01 * ظلام['ت']['value'] + 0.01 * ظلام['ظ']['value']
    ظلام['ز']['value'] += 0.01 * ظلام['ب']['value'] + 0.01 * ظلام['غ']['value']
    ظلام['ظ']['value'] += 0.01 * ظلام['ت']['value'] + 0.01 * ظلام['ذ']['value']
    ظلام['غ']['value'] += 0.01 * ظلام['ف']['value'] + 0.02 * ك_prev
    ظلام['ف']['value'] += 0.01 * ظلام['ظ']['value'] + 0.01 * ظلام['ش']['value']
    ظلام['و']['value'] += 0.01 * ظلام['ت']['value'] + 0.01 * ظلام['ذ']['value']
    ظلام['ش']['value'] += 0.01 * ظلام['ف']['value'] + 0.02 * ك_prev
    ظلام['ض']['value'] += 0.01 * ظلام['ج']['value'] + 0.01 * ظلام['ب']['value']

    # 3. النور يبدد الظلام
    for key in ['ظ', 'ش', 'ف', 'ج', 'ت', 'ذ', 'غ', 'خ', 'ب']:
        ظلام[key]['value'] *= (1 - 0.2 * نور['ن']['value'])
        ظلام[key]['value'] *= (1 - 0.2 * نور['ع']['value'])
        ظلام[key]['value'] *= (1 - 0.2 * نور['ل']['value'])

    # 4. تقييد القيم
    for key in نور: نور[key]['value'] = max(0.01, min(1.0, نور[key]['value']))
    for key in ظلام: ظلام[key]['value'] = max(0.01, min(1.0, ظلام[key]['value']))

    # 5. آلية الانهيار
    global collapse_triggered, collapse_year
    if not collapse_triggered and نور['هـ']['value'] < 0.1:
        collapse_triggered = True
        collapse_year = len(history_S)
        print(f"!!! {collapse_year}: عام الانهيار – {ظلام['ف']['label']} و{ظلام['ظ']['label']} طغى على {نور['هـ']['label']} !!!")
        نور['أ']['value'] = 0.1
        نور['ل']['value'] = 0.1

# =============================================
# المحاكاة الرئيسية
# =============================================
history_noor = {k: [] for k in نور}
history_dark = {k: [] for k in ظلام}

