def render_surah_maun():
    st.markdown("""
    ### ⚡ سورة الماعون – مختبر صدق العبادة

    > **"أَرَأَيْتَ الَّذِي يُكَذِّبُ بِالدِّينِ"** [الماعون: 1]

    **الدين** هنا هو **قانون السببية** (الدين القيم) و**يوم الجزاء** (يوم الدين).
    والتكذيب به يعني **إنكار أن للأعمال نتائج**. وهذا هو أصل كل فساد.

    ثم يكشف الله العلامة العملية لهذا التكذيب:

    > **"فَذَٰلِكَ الَّذِي يَدُعُّ الْيَتِيمَ * وَلَا يَحُضُّ عَلَىٰ طَعَامِ الْمِسْكِينِ"** [الماعون: 2-3]

    فهو **قاسٍ على الضعيف** (يدع اليتيم) و**بخيل على المحتاج** (لا يحض على طعام المسكين).

    ثم تأتي المفاجأة الكبرى:

    > **"فَوَيْلٌ لِّلْمُصَلِّينَ * الَّذِينَ هُمْ عَن صَلَاتِهِمْ سَاهُونَ * الَّذِينَ هُمْ يُرَاءُونَ * وَيَمْنَعُونَ الْمَاعُونَ"** [الماعون: 4-7]

    لم يقل "فويل للكافرين" أو "فويل لمن لا يصلي"، بل قال: **"فويل للمصلين"**!

    **سر المصلين:** صلاتهم لم تثمر. صلاتهم لم تولد طاقة كافية لتغيير سلوكهم. لماذا؟
    - **ساهون:** ضعف في **الولاء (W)** (لم يحسنوا الصلاة).
    - **يراؤون:** ضعف في **البراءة (B)** (طلب رضا الناس بدلاً من رضا الله).
    - **النتيجة الحتمية:** يمنعون الماعون – العطاء والمساعدة بأبسط الأشياء.

    **المعادلة في سورة الماعون:**
    > **W (صلاة بلا سهو) × B (إخلاص بلا رياء) = S (رحمة وعطاء)**
    > **W (صلاة مع سهو) × B (رياء) = 0 (ويل ومنع للماعون)**

    **الماعون هو الشيء اليسير الذي يتعاوره الناس. ومنعه هو العلامة النهائية على أن المعادلة مختلة: S = 0.**
    وحتى لو سقطت فريضة الزكاة عن إنسان لعدم بلوغه النصاب، فإن الله لم يتوقف عند هذا الحد، بل أشار إلى الماعون - أبسط ما يملكه الإنسان - ليكشف النوايا الباطلة.

    **الدرس:** الماعون هو اختبار صدق العبادة. إذا لم تثمر عبادتك رحمة وعطاءً، فراجع معادلة ولائك وبراءتك.
    """, unsafe_allow_html=True)


